/**
 * Offline ingestion pipeline — build a scaled, license-tagged evidence index.
 *
 * Deterministic and reproducible: reads the frozen curated tier
 * (backend/data/evidence_records.json + synonym_index.json) verbatim, then
 * folds in license-tagged seed interaction facts, deduplicating by canonical
 * (hormonal concept, medication concept) pair. Emits the expanded artifacts and
 * a build manifest (counts, license/severity breakdown, source list, content
 * hash) under backend/data/generated/.
 *
 * SAFETY: every emitted record is source-linked and physicianVerified:false
 * (pending sign-off). No medical claim is generated by a model; the pipeline
 * only normalizes, tags, dedups and indexes curated evidence. The deterministic
 * runtime lookup (EvidenceIndex.lookupPair) is unchanged — this just populates
 * the same artifact shape at larger scale.
 */
import { createHash } from 'node:crypto';
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const HERE = path.dirname(fileURLToPath(import.meta.url));
const BACKEND_DIR = path.resolve(HERE, '..');
const DATA_DIR = path.join(BACKEND_DIR, 'data');
const OUT_DIR = path.join(DATA_DIR, 'generated');

const ALLOWED_LICENSE_TAGS = new Set(['public_domain', 'curated_prototype', 'research_nc']);

/** Fields the loader (EvidenceIndex) requires on every record. */
const REQUIRED_FIELDS = [
  'id', 'hormonalProduct', 'hormonalSynonyms', 'interactingMedication', 'medicationSynonyms',
  'interactionDirection', 'potentialConsequence', 'clinicianConsideration', 'evidenceLevel',
  'population', 'sourceTitle', 'sourceOrganization', 'sourceUrl', 'sourceSection', 'jurisdiction',
  'lastVerified', 'physicianVerified', 'limitations',
  'matchType', 'hormonalConceptIds', 'interactingConceptIds', 'interactionDirectionCode',
];

type Json = Record<string, any>;

function pairKey(h: string, m: string): string {
  return `${h} ${m}`;
}

/** Every (hormonal, medication) concept pair a record contributes. */
function recordPairs(record: Json): string[] {
  const pairs: string[] = [];
  for (const h of record.hormonalConceptIds ?? []) {
    for (const m of record.interactingConceptIds ?? []) pairs.push(pairKey(h, m));
  }
  return pairs;
}

/** Stable stringify (sorted keys) so the content hash is deterministic. */
function stableStringify(value: unknown): string {
  if (Array.isArray(value)) return `[${value.map(stableStringify).join(',')}]`;
  if (value && typeof value === 'object') {
    const keys = Object.keys(value as Json).sort();
    return `{${keys.map((k) => `${JSON.stringify(k)}:${stableStringify((value as Json)[k])}`).join(',')}}`;
  }
  return JSON.stringify(value);
}

export interface BuildResult {
  records: Json[];
  ontology: Json;
  manifest: Json;
  problems: string[];
}

/**
 * Build the expanded index in memory (no file writes). The benchmark's index
 * layer calls this so the fixed run command needs no separate ingest step.
 */
export function buildGeneratedIndex(): BuildResult {
  const problems: string[] = [];

  const curated = JSON.parse(readFileSync(path.join(DATA_DIR, 'evidence_records.json'), 'utf8')) as Json;
  const ontology = JSON.parse(readFileSync(path.join(DATA_DIR, 'synonym_index.json'), 'utf8')) as Json;
  const seed = JSON.parse(readFileSync(path.join(HERE, 'sources', 'interaction_facts.json'), 'utf8')) as Json;

  const hormonalConcepts: Json = ontology.hormonalConcepts ?? {};
  const medicationConcepts: Json = ontology.medicationConcepts ?? {};

  // The frozen curated tier passes through byte-for-byte (top trust).
  const curatedRecords: Json[] = (curated.records ?? []).map((r: Json) => ({
    ...r,
    licenseTag: r.licenseTag ?? 'curated_prototype',
    tier: 'curated',
  }));

  const seenPairs = new Set<string>();
  for (const r of curatedRecords) for (const p of recordPairs(r)) seenPairs.add(p);

  const generatedRecords: Json[] = [];
  let skippedDuplicatePairs = 0;
  let skippedRecords = 0;

  for (const fact of seed.facts ?? []) {
    // Validate concept references against the approved ontology.
    for (const h of fact.hormonalConceptIds ?? []) {
      if (!(h in hormonalConcepts)) problems.push(`${fact.id}: unknown hormonal concept ${h}`);
    }
    for (const m of fact.interactingConceptIds ?? []) {
      if (!(m in medicationConcepts)) problems.push(`${fact.id}: unknown medication concept ${m}`);
    }
    if (!ALLOWED_LICENSE_TAGS.has(fact.licenseTag)) {
      problems.push(`${fact.id}: invalid licenseTag ${JSON.stringify(fact.licenseTag)}`);
    }
    for (const f of REQUIRED_FIELDS) {
      if (!(f in fact)) problems.push(`${fact.id}: missing required field ${f}`);
    }

    // Deduplicate by canonical pair: keep only the pairs not already covered.
    const newMeds = (fact.interactingConceptIds ?? []).filter((m: string) =>
      (fact.hormonalConceptIds ?? []).some((h: string) => !seenPairs.has(pairKey(h, m))),
    );
    const contributesNewPair = (fact.hormonalConceptIds ?? []).some((h: string) =>
      (fact.interactingConceptIds ?? []).some((m: string) => !seenPairs.has(pairKey(h, m))),
    );
    if (!contributesNewPair) {
      skippedRecords++;
      continue;
    }
    for (const p of recordPairs(fact)) {
      if (seenPairs.has(p)) skippedDuplicatePairs++;
      seenPairs.add(p);
    }

    const { severityOrdinal, licenseTag, ...recordFields } = fact;
    generatedRecords.push({
      ...recordFields,
      severityOrdinal,
      licenseTag,
      tier: 'generated',
      provenance: [
        {
          source: fact.sourceOrganization,
          sourceTitle: fact.sourceTitle,
          sourceUrl: fact.sourceUrl,
          licenseTag,
          severityOrdinal,
        },
      ],
    });
    void newMeds;
  }

  const records = [...curatedRecords, ...generatedRecords];

  // Build breakdowns for the manifest.
  const licenseBreakdown: Record<string, number> = {};
  const severityBreakdown: Record<string, number> = {};
  const sources = new Set<string>();
  let pairCount = 0;
  const allPairs = new Set<string>();
  for (const r of records) {
    licenseBreakdown[r.licenseTag] = (licenseBreakdown[r.licenseTag] ?? 0) + 1;
    const sev = r.severityOrdinal ?? 'unscored';
    severityBreakdown[String(sev)] = (severityBreakdown[String(sev)] ?? 0) + 1;
    sources.add(String(r.sourceOrganization));
    for (const p of recordPairs(r)) {
      pairCount++;
      allPairs.add(p);
    }
  }

  const manifest: Json = {
    builtBy: 'backend/ingest/build_index.ts',
    curatedDatasetVersion: curated.datasetVersion ?? 'unknown',
    seedSetVersion: seed.sourceSetVersion ?? 'unknown',
    recordCount: records.length,
    curatedRecordCount: curatedRecords.length,
    generatedRecordCount: generatedRecords.length,
    hormonalConceptCount: Object.keys(hormonalConcepts).length,
    medicationConceptCount: Object.keys(medicationConcepts).length,
    distinctPairCount: allPairs.size,
    totalPairEmissions: pairCount,
    skippedDuplicatePairs,
    skippedRecordsAllDuplicate: skippedRecords,
    licenseBreakdown,
    severityBreakdown,
    distinctSources: [...sources].sort(),
    commercialSafeRecords: licenseBreakdown['public_domain'] ?? 0,
    contentHash: '',
    problems,
  };
  manifest.contentHash = createHash('sha256')
    .update(stableStringify({ records, ontology }))
    .digest('hex')
    .slice(0, 16);

  return { records, ontology, manifest, problems };
}

export interface GeneratedArtifacts {
  evidencePath: string;
  synonymPath: string;
  manifestPath: string;
  manifest: Json;
  problems: string[];
}

/** Write the generated artifacts to backend/data/generated/ and return their paths. */
export function writeGeneratedArtifacts(): GeneratedArtifacts {
  const { records, ontology, manifest, problems } = buildGeneratedIndex();
  mkdirSync(OUT_DIR, { recursive: true });
  const dataset = {
    datasetVersion: 'generated-0.1.0',
    generatedNote:
      'Built by backend/ingest/build_index.ts from the frozen curated tier + license-tagged seed facts. ' +
      'All records source-linked and physicianVerified:false (pending sign-off).',
    records,
  };
  const evidencePath = path.join(OUT_DIR, 'evidence_records.json');
  const synonymPath = path.join(OUT_DIR, 'synonym_index.json');
  const manifestPath = path.join(OUT_DIR, 'build_manifest.json');
  writeFileSync(evidencePath, JSON.stringify(dataset, null, 2) + '\n');
  writeFileSync(synonymPath, JSON.stringify(ontology, null, 2) + '\n');
  writeFileSync(manifestPath, JSON.stringify(manifest, null, 2) + '\n');
  return { evidencePath, synonymPath, manifestPath, manifest, problems };
}

const isMain =
  process.argv[1] !== undefined && import.meta.url === pathToFileURL(path.resolve(process.argv[1])).href;
if (isMain) {
  const { manifest, problems } = writeGeneratedArtifacts();
  console.log(`[ingest] wrote ${manifest.recordCount} records to ${OUT_DIR}`);
  console.log(`[ingest] manifest ${JSON.stringify(manifest)}`);
  if (problems.length) {
    console.error(`[ingest] ${problems.length} problem(s): ${JSON.stringify(problems)}`);
    process.exit(1);
  }
}
