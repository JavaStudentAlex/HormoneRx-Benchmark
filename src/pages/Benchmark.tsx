import { useMemo, useState } from 'react';
import { Card, CardBody, CardHeader, CardTitle, Stat, Badge, cn } from '../components/ui/primitives';
import results from '../data/benchmark_results.json';
import backendResults from '../data/streaming_benchmark_results.json';

const R = results as any;
const B = backendResults as any;

const CATEGORIES = ['all', 'clear_positive', 'implicit_positive', 'true_negative', 'explicit_negation', 'historical_use', 'ambiguous_missing'];
const OUTCOMES = ['all', 'pass', 'fail'];

export default function Benchmark() {
  const m = R.metrics;
  const meta = R.metadata;
  const [category, setCategory] = useState('all');
  const [outcome, setOutcome] = useState('all');

  const cases = useMemo(() => {
    return (R.cases as any[]).filter((c) => {
      if (category !== 'all' && c.category !== category) return false;
      if (outcome === 'pass' && !c.pass) return false;
      if (outcome === 'fail' && c.pass) return false;
      return true;
    });
  }, [category, outcome]);

  const pct = (n: number) => `${Math.round(n * 100)}%`;

  return (
    <div className="space-y-6">
      <div>
        <h1 className="text-2xl font-semibold tracking-tight text-navy">Benchmark</h1>
        <p className="mt-2 max-w-3xl text-sm text-navy-soft">
          Metrics are read from <span className="font-mono text-xs">src/data/benchmark_results.json</span>, generated by the
          reproducible runner. Nothing on this page is hardcoded.
        </p>
        <div className="mt-2 flex flex-wrap gap-1.5">
          <Badge tone="muted">mode: {meta.mode}</Badge>
          <Badge tone="muted">model: {meta.model}</Badge>
          <Badge tone="muted">benchmark v{meta.benchmarkVersion}</Badge>
          <Badge tone="muted">evidence v{meta.evidenceVersion}</Badge>
          <Badge tone="muted">pipeline v{meta.pipelineVersion}</Badge>
          <Badge tone="muted">seed {meta.randomSeed}</Badge>
        </div>
        <p className="mt-1 text-[11px] text-ink-faint">Evaluated {new Date(meta.evalTimestamp).toLocaleString()} · {meta.caseCount} cases · prompt: {meta.promptVersion}</p>
      </div>

      <section className="grid grid-cols-2 gap-3 md:grid-cols-4">
        <Stat label="Trigger precision" value={pct(m.triggerPrecision)} tone="teal" />
        <Stat label="Trigger recall" value={pct(m.triggerRecall)} tone="teal" />
        <Stat label="Trigger F1" value={pct(m.triggerF1)} tone="teal" />
        <Stat label="Retrieval accuracy" value={pct(m.retrievalAccuracy)} tone="teal" />
        <Stat label="Correct abstention" value={pct(m.correctAbstentionRate)} tone="teal" />
        <Stat label="Negation accuracy" value={pct(m.negationAccuracy)} tone="teal" />
        <Stat label="Historical accuracy" value={pct(m.historicalContextAccuracy)} tone="teal" />
        <Stat label="Citation coverage" value={pct(m.citationCoverage)} tone="teal" />
        <Stat label="Entity precision" value={pct(m.entityPrecision)} tone="navy" />
        <Stat label="Entity recall" value={pct(m.entityRecall)} tone="navy" />
        <Stat label="False positives" value={m.falsePositiveCount} tone={m.falsePositiveCount === 0 ? 'navy' : 'amber'} />
        <Stat label="Unsupported claims" value={m.unsupportedClaimCount} tone={m.unsupportedClaimCount === 0 ? 'navy' : 'amber'} />
      </section>

      <Card>
        <CardHeader className="flex flex-wrap items-center justify-between gap-3">
          <CardTitle>Case-level results</CardTitle>
          <div className="flex flex-wrap gap-2">
            <label className="text-xs text-ink-muted">
              Category
              <select value={category} onChange={(e) => setCategory(e.target.value)} className="ml-1 rounded border border-line bg-surface px-2 py-1 text-xs text-navy">
                {CATEGORIES.map((c) => <option key={c} value={c}>{c}</option>)}
              </select>
            </label>
            <label className="text-xs text-ink-muted">
              Outcome
              <select value={outcome} onChange={(e) => setOutcome(e.target.value)} className="ml-1 rounded border border-line bg-surface px-2 py-1 text-xs text-navy">
                {OUTCOMES.map((o) => <option key={o} value={o}>{o}</option>)}
              </select>
            </label>
          </div>
        </CardHeader>
        <CardBody className="overflow-x-auto p-0">
          <table className="w-full min-w-[860px] border-collapse text-sm">
            <thead>
              <tr className="border-b border-line bg-canvas text-left text-xs uppercase tracking-wide text-ink-muted">
                <th className="px-4 py-2.5">ID</th>
                <th className="px-4 py-2.5">Category</th>
                <th className="px-4 py-2.5">Expected state</th>
                <th className="px-4 py-2.5">Actual state</th>
                <th className="px-4 py-2.5">Expected rec</th>
                <th className="px-4 py-2.5">Retrieved rec</th>
                <th className="px-4 py-2.5">Result</th>
              </tr>
            </thead>
            <tbody>
              {cases.map((c) => (
                <tr key={c.id} className="border-b border-line align-top hover:bg-canvas/60">
                  <td className="px-4 py-2.5 font-mono text-xs">{c.id}</td>
                  <td className="px-4 py-2.5 text-xs">{c.category}</td>
                  <td className="px-4 py-2.5 font-mono text-[11px] text-ink-muted">{c.expectedResultState}</td>
                  <td className="px-4 py-2.5 font-mono text-[11px]">{c.actualResultState}</td>
                  <td className="px-4 py-2.5 font-mono text-xs">{c.expectedEvidenceRecordId ?? '—'}</td>
                  <td className="px-4 py-2.5 font-mono text-xs">{c.actualEvidenceRecordId ?? '—'}</td>
                  <td className="px-4 py-2.5">
                    <Badge tone={c.pass ? 'teal' : 'danger'}>{c.pass ? 'pass' : 'fail'}</Badge>
                  </td>
                </tr>
              ))}
              {cases.length === 0 && (
                <tr><td colSpan={7} className="px-4 py-4 text-ink-faint">No cases match the current filter.</td></tr>
              )}
            </tbody>
          </table>
        </CardBody>
      </Card>

      <p className="text-xs text-ink-muted">
        A high pass rate on this deterministic demo pipeline reflects that the synthetic cases use the dataset's own
        vocabulary; it does not establish clinical accuracy. The benchmark's value is the labelled harness — especially the
        negation, temporality, and correct-abstention checks — which a live model can be scored against.
      </p>

      <BackendBenchmark />
    </div>
  );
}

// Realtime backend benchmark (TypeScript engine): Layer A text v2 + Layer B streaming.
// Read from src/data/streaming_benchmark_results.json, generated by
// `npm run benchmark:backend`.
function BackendBenchmark() {
  const pct = (n: number) => `${Math.round(n * 100)}%`;
  const text = B.layers?.text;
  const streaming = B.layers?.streaming;
  const audio = B.layers?.audio;
  if (!streaming) return null;
  const sm = streaming.metrics;
  return (
    <section className="space-y-4 border-t border-line pt-6">
      <div>
        <h2 className="text-lg font-semibold tracking-tight text-navy">Realtime backend benchmark (streaming engine)</h2>
        <p className="mt-1 max-w-3xl text-sm text-navy-soft">
          The TypeScript encounter engine is evaluated on a text layer ({text?.caseCount ?? 0} cases) and a streaming layer
          ({streaming.caseCount} multi-turn sequences with an expected state after every event: corrections, negations,
          late events, duplicate replays, proposal lifecycle).
        </p>
        <div className="mt-2 flex flex-wrap gap-1.5">
          <Badge tone="muted">engine: {B.metadata.engine}</Badge>
          <Badge tone="muted">extractor: {B.metadata.extractionModel}</Badge>
          <Badge tone="amber">physician sign-off pending: {B.metadata.pendingPhysicianSignOff.length} records</Badge>
        </div>
      </div>
      <div className="grid grid-cols-2 gap-3 md:grid-cols-4">
        <Stat label="Streaming case pass" value={pct(sm.casePassRate)} tone="teal" />
        <Stat label="Per-event state accuracy" value={pct(sm.perEventStateAccuracy)} tone="teal" />
        <Stat label="Retraction accuracy" value={pct(sm.warningRetractionAccuracy)} tone="teal" />
        <Stat label="Premature warnings" value={sm.prematureWarningCount} tone={sm.prematureWarningCount === 0 ? 'navy' : 'amber'} />
        <Stat label="Duplicate warnings" value={sm.duplicateWarningCount} tone={sm.duplicateWarningCount === 0 ? 'navy' : 'amber'} />
        <Stat label="Citation coverage" value={pct(sm.citationCoverage)} tone="teal" />
        <Stat label="Unsupported claims" value={sm.unsupportedClaimCount} tone={sm.unsupportedClaimCount === 0 ? 'navy' : 'amber'} />
        {text && <Stat label="Text layer pass (v2)" value={pct(text.metrics.passRate)} tone="teal" />}
      </div>
      {streaming.processingLatency && (
        <p className="text-xs text-ink-muted">
          Backend processing latency (deterministic extractor, no network): median{' '}
          {streaming.processingLatency.medianMs} ms · p90 {streaming.processingLatency.p90Ms} ms · max{' '}
          {streaming.processingLatency.maxMs} ms over {streaming.processingLatency.samples} turns. Live-model latency is
          not included and must be measured separately.
        </p>
      )}
      {audio?.status === 'SKIPPED' && (
        <p className="text-xs text-ink-muted">
          Audio layer (Layer C): <span className="font-medium">not executed</span> — {audio.reason}. Gold labels are frozen
          in <span className="font-mono">backend/data/audio_benchmark_manifest.json</span>; results are only reported for
          runs that actually happened.
        </p>
      )}
    </section>
  );
}
